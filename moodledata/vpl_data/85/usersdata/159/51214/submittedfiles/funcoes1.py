def crescente (lista):
    for i in range (1,len(lista),1):
        if a[i]<=a[i-1]:
            return False
    return True     
    
def decrescente (lista):
    for i in range (1,len(lista),1):
        if a[i]>=a[i-1]:
            return False
    return True        

def igual (lista):
    for i in range (1,len(lista),1):
        if a[i]==a[i-1]:
            return False
    return True        





