peso=int(input('digite o peso em kilos:'))
altura=(input('digite a altura em metros:'))
imc==peso/altura*altura
if imc<20:
    print('ABAIXO')
if 20<=imc<=25:
    print('NORMAL')
if 25<=imc<=30:
    print('SOBREPESO')
if 30<imc<=40:
    print('OBESIDADE')
if imc>40:
    print('OBESIDADE GRAVE')

