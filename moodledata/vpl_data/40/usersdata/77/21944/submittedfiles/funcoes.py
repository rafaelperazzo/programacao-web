#CÁCULO DO FATORIAL PERTENCENTE A FORMULA DO COS X.
#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#OS FATORIAS ESTÃO PRESENTES NA FORMULA DO COS X, ONDE FICAM LOCALIZADOS NOS DENOMINADORES.
#NA FORMULA, COMEÇA PELO 2! NO SEGUNDO TERMO,APÓS 4!, 6!...E ASSIM POR DIANTE, SEMPRE OS VALORES SÃO NÚMEROS PARES.
#NA FORMULA O FATORIAL DE 1 ESTÁ PRESENTE NO PRIMEIRO TERMO, COMO O FATORIAL DE 1 É O PRÓPRIO, ENTÃO O FATORIAL COMEÇA COM UM JÁ ACRESCIDO EM SEU VALOR.
#A PARTIR DAI MONTA-SE O CALCULO DOS DEMAIS FATORAIS

#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

def fatorial(a):
    fatorial=1
    for i in range(1,a+1,1):
        fatorial=fatorial*i
        
    return fatorial

