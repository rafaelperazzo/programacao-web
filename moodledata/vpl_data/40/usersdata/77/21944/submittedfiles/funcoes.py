#CALCULO DO VALOR ABSOLUTO DE X.
#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#COMO É EXTRITAMENTE PROIBIDO A UTILIZAÇÃO DE BIBLIOTECAS QUE FACILITEM O CALCULO DO MODULO DO NÚMERO, USA-SE ESSA FUNÇÃO.
#É INSERIDO UM PARAMETRO X, E LOGO PARTE PARA AS CONDIÇÕES.
#SE O VALOR DO X FOR EXTRITAMENTE ABAIXO DE ZERO,OU SEJA, NEGATIVO, MULTIPLICA-SE ESSE VALOR POR (-1),LOGO APÓS RETORNA O VALOR X.
#SE O VALOR DO X FOR EXTRITAMENTE ACIMA DE ZERO, OU SEJA,POSITIVO, APENAS RETORNA O VALOR DE X.

#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

def calcula_valor_absoluto(x):
    if x<0:
        x=x*(-1)
        return x
    else:
        return x

