#CÁLCULO DA RAZÃO ÁUREA
#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#ÚLTIMA FUNÇÃO A SER FEITA, E A MAIS IMPORTANTE. RESPONSÁVEL PELO CÁLCULO FINAL DA RAZÃO ÁUREA.
#UTILIZA-SE UMA FORMULA DA RAZÃO AUREA, QUE É IGUAL 2 VEZES O COSSENO DE PI, DIVIDIDO POR 5.0.

#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

def calcula_razao_aurea(m,e):
    
    razao_aurea=2*(calcula_co_seno(calcula_pi(m)/5.0,e))
    
    return razao_aurea
    
    
