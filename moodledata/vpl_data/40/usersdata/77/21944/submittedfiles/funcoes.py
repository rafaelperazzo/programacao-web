#CÁLCULO_CO_SENO
#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

#A PARTIR DOS PARAMETROS Z E e, FACILITA O CÁLCULO DO COS Z
#CRIA-SE UMA CONDIÇÃO PARA QUE O EPSILON ESTEJA ENTRE O 0 E O 1, USANDO O WHILE.
#AH UMA ALTERNÂNCIA DE TERMOS E SEUS RESPECTIVOS SINAIS PRECEDENTES, OS IMPARES NEGATIVOS E OS PARES POSITIVOS.
#NO FINAL RETORNA O VALOR FINAL DO COS

#------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

def calcula_co_seno(z,e):
    i=1
    j=2
    soma=0
    termo=((z**j)/fatorial(j))
    while termo>e:
        if i%2!=0:
            soma=soma-termo
        else:
            soma=soma+termo
        
        i=i+1
        j=j+2
        termo=((z**j)/fatorial(j))
        
    cosseno=1+soma
    return cosseno
    
