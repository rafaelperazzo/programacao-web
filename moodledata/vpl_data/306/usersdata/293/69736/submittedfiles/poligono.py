print("Este programa calcula o número de diagonais diferentes de um polígono convexo")
n=int(input("Digite o número de lados de um polígono convexo qualquer: "))
d=((n*(n-3))/2)
print("O numero de diagonais diferentes sao: "+"%.1f"%d)
